package entity;

/**
 *
 * @author Minami
 */
public class PerkiraanEntity {
    String kode,nama,tipe;

    public PerkiraanEntity(String kode, String nama, String tipe) {
        this.kode = kode;
        this.nama = nama;
        this.tipe = tipe;
    }

    public String getKode() {
        return kode;
    }

    public void setKode(String kode) {
        this.kode = kode;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getTipe() {
        return tipe;
    }

    public void setTipe(String tipe) {
        this.tipe = tipe;
    }
    
}
